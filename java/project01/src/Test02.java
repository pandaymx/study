public class Test02 {
    public static void main(String[] args) {
        // 整数字面量
        System.out.println(6);
        // 小数字面量
        System.out.println(6.0);
        // 字符串字面量
        System.out.println("6");
        // 字符字面量
        System.out.println('6');
        // 布尔型字面量
        System.out.println(true);
    }
}
